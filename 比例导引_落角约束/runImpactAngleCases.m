    "missDistanceM", NaN, "closestTimeS", NaN, ...
    "terminalThetaDeg", NaN, "terminalPsiDeg", NaN, ...
    "thetaErrorDeg", NaN, "psiErrorDeg", NaN, "hit", false);
end
