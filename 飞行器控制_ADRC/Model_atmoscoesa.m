function [T,P,rou,a] = Model_atmoscoesa(H)    %H的单位为m,V的单位为m/s
%=====大气模型和温度模型=====
%=====创建时间2018.09.13======
%=====修改时间2018.09.13=======
T_SL = 288.15;     %单位为 K
P_SL = 1.01325e5;  %单位为 N/m^2=Pa
rou_SL = 1.2250;    %单位为 kg/m^3
g_SL = 9.80665;     %单位为 /s^2
a_SL = 340.294;     %单位为 m/s
R = 6356.766;

H = H/1000;  %单位转换为km
Z = H/(1 + H/R); %H为几何高度 Z为位势高度

if Z>=0 && Z <= 11.0191 
    W = 1 - Z/44.3048;
    T = 288.15*W;
    P = (W^5.2553)*P_SL;
    rou = (W^4.2559)*rou_SL;

elseif Z>=0 && Z <= 20.0631
    W = exp((14.9647-Z)/6.3416);
    T = 216.650;
    P = (1.1953e-1)*W*P_SL;
    rou = (1.5898e-1)*W*rou_SL;

elseif Z>=0 && Z <= 32.1619
    W = 1 + (Z-24.9021)/221.552;
    T = 221.552*W;
    P = (2.5158e-2)*(W^(-34.1629))*P_SL;
    rou = (3.2772e-2)*(W^(-35.1629))*rou_SL;

elseif Z>=0 && Z <= 47.3501
    W = 1 + (Z-39.7499)/89.4107;
    T = 250.350*W;
    P = (2.8338e-3)*(W^(-12.2011))*P_SL;
    rou = (3.2618e-3)*(W^(-13.2011))*rou_SL;   
  
elseif Z>=0 && Z <= 51.4125
    W = exp((48.6252-Z)/7.9223);
    T = 270.650;
    P = (8.9155e-4)*W*P_SL;
    rou = (9.4920e-4)*W*rou_SL;   
    
elseif Z>=0 && Z <= 71.8020
    W = 1 - (Z-59.4390)/88.2218;
    T = 247.021*W;
    P = (2.1671e-4)*(W^(12.2011))*P_SL;
    rou = (2.5280e-4)*(W^(11.2011))*rou_SL;
    
elseif Z>=0 && Z <= 86.0000
    W = 1 - (Z-78.0303)/100.2950;
    T = 200.590*W;
    P = (1.2274e-5)*(W^(17.0816))*P_SL;
    rou = (1.7632e-5)*(W^(16.0816))*rou_SL; 
    
elseif Z>=0 && Z <= 91.0000
    W = exp((87.2848-Z)/5.4700);
    T = 186.870;
    P = (2.2730 + (1.042e-2)*Z)*(1e-6)*W*P_SL;
    rou = (3.6411e-6)*W*rou_SL; 
end
a = 20.0468*sqrt(T);
end